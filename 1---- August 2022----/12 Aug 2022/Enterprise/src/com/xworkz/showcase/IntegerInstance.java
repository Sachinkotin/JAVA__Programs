package com.xworkz.showcase;

public class IntegerInstance {

	public static void main(String[] args) {

		//Object creation is needed 
		
		Integer insta=new Integer(549);
		
		
		byte k=insta.byteValue();
		System.out.println(k);
		
		int a=insta.compareTo(52);
		System.out.println(a);
		
		double b=insta.doubleValue();
		System.out.println(b);
		
		boolean c=insta.equals(52);
		System.out.println(c);
	
		float d=insta.floatValue();
		System.out.println(d);
		
		int e=insta.hashCode();
		System.out.println(e);
		
		int f=insta.intValue();
		System.out.println(f);
		
		long g=insta.longValue();
		System.out.println(g);
		
		short h=insta.shortValue();
		System.out.println(h);
		
		String i=insta.toString();
		System.out.println(i);
		
		Integer inta=new Integer("452");
		
		byte l=inta.byteValue();
		System.out.println(l);
		
		int m=inta.compareTo(52);
		System.out.println(m);
		
		double n=inta.doubleValue();
		System.out.println(n);
		
		boolean o=inta.equals(52);
		System.out.println(o);
	
		float p=inta.floatValue();
		System.out.println(p);
		
		int q=inta.hashCode();
		System.out.println(q);
		
		int r=inta.intValue();
		System.out.println(r);
		
		long s=inta.longValue();
		System.out.println(s);
		
		short t=inta.shortValue();
		System.out.println(t);
		
		String u=inta.toString();
		System.out.println(u);
		
	
	}

}
